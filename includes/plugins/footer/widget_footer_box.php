// Holt die Daten für das Plugin
$pluginData = \webspell\PluginService::getPluginData("footer", $plugin_path);
// Zugriff auf die Sprachdateien und das Template
$plugin_language = $pluginData['language'];
$plugin_template = $pluginData['template'];

$qs_arr = array();
parse_str($_SERVER['QUERY_STRING'], $qs_arr);

$getsite = 'startpage'; // Wird auf der Startseite angezeigt index.php
if (isset($qs_arr['site']) && !empty($qs_arr['site'])) {
    $getsite = htmlspecialchars($qs_arr['site'], ENT_QUOTES, 'UTF-8'); // Schutz vor XSS
}

// Hole die aktiven Erweiterungen
$ergebnis = safe_query("SELECT * FROM " . PREFIX . "settings_expansion WHERE active = '1'");
if (mysqli_num_rows($ergebnis) > 0) {
    $ds = mysqli_fetch_array($ergebnis);

    // Sicherstellen, dass der Pfad und das Bild existieren
    $pfad = isset($ds['pfad']) ? htmlspecialchars($ds['pfad'], ENT_QUOTES, 'UTF-8') : '';
    $logo_pic = isset($ds['logo_pic']) ? htmlspecialchars($ds['logo_pic'], ENT_QUOTES, 'UTF-8') : '';

    // Bild-Tag erstellen, wenn die Daten vorhanden sind
    if ($pfad && $logo_pic) {
        $img = '<img class="img-fluid" style="height:90px" src="../includes/expansion/' . $pfad . '/images/' . $logo_pic . '" alt="">';
    } else {
        // Standardbild oder Platzhalter, falls Daten fehlen
        $img = '<img class="img-fluid" style="height:90px" src="../includes/expansion/default/images/default_logo.png" alt="">';
    }

    // Daten für das Template vorbereiten
    $data_array = array();
    $data_array['img'] = $img; // Korrekt den Schlüssel ohne Anführungszeichen

    // Template laden und ausgeben
    $template = $plugin_template->loadTemplate("footer_box", "content", $data_array, $plugin_path);
    echo $template;
} else {
    // Falls keine aktiven Erweiterungen gefunden wurden, Fehlerbehandlung oder Standard-Template
    echo '<p>Keine Erweiterungen verfügbar.</p>';
}

